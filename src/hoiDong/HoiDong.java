package hoiDong;

public class HoiDong {
	private String tenHoiDong, thoiGianThanhLap;

	public HoiDong(String tenHoiDong, String thoiGianThanhLap) {
		super();
		this.tenHoiDong = tenHoiDong;
		this.thoiGianThanhLap = thoiGianThanhLap;
	}

	public String getTenHoiDong() {
		return tenHoiDong;
	}

	public void setTenHoiDong(String tenHoiDong) {
		this.tenHoiDong = tenHoiDong;
	}

	public String getThoiGianThanhLap() {
		return thoiGianThanhLap;
	}

	public void setThoiGianThanhLap(String thoiGianThanhLap) {
		this.thoiGianThanhLap = thoiGianThanhLap;
	}
	
	
	
}
